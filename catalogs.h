int ngcLookup(int ncgId) {
  // binary search 
  int first = 0;
  int last  = ELEMENTS(NGCs);
  int mid;
  int id;
  while ( first <= last ) {
    mid = (first + last)/2;
    id = (int) pgm_read_word(&(NGCs[mid].id));
    if (id < ncgId ) {
      first = mid+1;
    } else if (id > ncgId ) {
      last = mid-1;
    } else { // found!
      return mid;
    }
  }
  return -1; // not found
}
